// «аполн€ем частотный словарь
void InvertedIndex::PutFreqDictionary(std::string inWords, int i) {
    Entry entry{};
    std::string oneWord;
    std::istringstream words(inWords);
    int countWords = 0;
    // –азбиваем строку на отдельные слова и заполн€ем частотный словарь
    while (std::getline(words, oneWord, ' '))
    {
        std::vector<Entry> vectorEntry;
        std::lock_guard<std::mutex> guard(addingDate);
        // »гнорируем слово и продолжаем индексацию, если данное слово больше 100 символов
        if (oneWord.length() > 100)
        {
            std::cerr << "No-correct size words in file #" << i << std::endl;
            continue;
        }
        // ѕрекращаем цикл, игнориру€ продолжение документа, если счетчик слов в файле больше 1000
        if (countWords > 1000)
        {
            std::cerr << "No-correct size file #" << i << std::endl;
            break;
        }
        // ƒобавл€ем новую пару, если данное слово не найдено в частотном словаре
        if (!freqDictionary.count(oneWord))
        {
            entry.docId = i;
            entry.count = 1;
            vectorEntry.push_back(entry);

            freqDictionary.insert({ oneWord, vectorEntry });

        }
        else // ѕровер€ем данное слово на наличие в этом документе
        {
            auto it = freqDictionary.find(oneWord);
            bool found = false;
            for (auto& k : it->second)
            {
                // ”величиваем количество, если данное слово уже встречалось в данном документе
                if (k.docId == i)
                {
                    k.count++;
                    found = true;
                    break;
                }
            }
            // ƒобавл€ем новую пару, если данное слово не встречалось в данном документе
            if (!found)
            {
                entry.docId = i;
                entry.count = 1;
                it->second.push_back(entry);
            }
        }
        ++countWords;
    }
}

